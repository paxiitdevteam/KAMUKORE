// Get current language from URL or localStorage
function getCurrentLanguage() {
  const urlParams = new URLSearchParams(window.location.search);
  const langParam = urlParams.get('lang');
  if (langParam && (langParam === 'en' || langParam === 'fr')) {
    localStorage.setItem('kamukore-language', langParam);
    return langParam;
  }
  const stored = localStorage.getItem('kamukore-language');
  if (stored && (stored === 'en' || stored === 'fr')) {
    return stored;
  }
  // Default to English
  return 'en';
}

// Set language and update page
function setLanguage(lang) {
  if (lang !== 'en' && lang !== 'fr') return;
  
  localStorage.setItem('kamukore-language', lang);
  
  // Update URL without reload
  const url = new URL(window.location);
  url.searchParams.set('lang', lang);
  window.history.pushState({}, '', url);
  
  // Update page content
  translatePage(lang);
  
  // Update language switcher
  updateLanguageSwitcher(lang);
}

// Translate page content
function translatePage(lang) {
  const currentLang = translations[lang] || translations.en;
  const currentYear = new Date().getFullYear();
  
  // Translate all elements with data-translate attribute
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    if (currentLang[key]) {
      let translatedText = currentLang[key];
      // Replace {year} placeholder with current year
      translatedText = translatedText.replace('{year}', currentYear);
      element.textContent = translatedText;
    }
  });
  
  // Update HTML lang attribute
  document.documentElement.lang = lang;
  
  // Update meta description if exists
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && currentLang['meta.description']) {
    metaDesc.content = currentLang['meta.description'];
  }
  
  // Also update copyright year for elements that might not have data-translate
  updateCopyrightYear();
}

// Update language switcher active state
function updateLanguageSwitcher(lang) {
  document.querySelectorAll('.lang-switcher a').forEach(link => {
    link.classList.remove('active');
    if (link.getAttribute('data-lang') === lang) {
      link.classList.add('active');
    }
  });
}

// Initialize translation on page load
function initTranslation() {
  const lang = getCurrentLanguage();
  translatePage(lang);
  updateLanguageSwitcher(lang);
}

// Update copyright year helper function
function updateCopyrightYear() {
  const currentYear = new Date().getFullYear();
  const copyrightElements = document.querySelectorAll('.footer-copyright');
  
  copyrightElements.forEach(function(element) {
    if (element.textContent && element.textContent.includes('{year}')) {
      element.textContent = element.textContent.replace('{year}', currentYear);
    } else if (element.textContent && /\d{4}/.test(element.textContent)) {
      // Replace any 4-digit year with current year
      element.textContent = element.textContent.replace(/\d{4}/, currentYear);
    }
  });
}

